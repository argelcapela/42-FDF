#include "../../fdf.h"

void    add_move(int key, t_fdf **fdf)
{
	if (key == 65364)
		(*fdf)->move_y += 10;
	if (key == 65362)
		(*fdf)->move_y -= 10;
	if (key == 65361)
		(*fdf)->move_x -= 10;
	if (key == 65363)
		(*fdf)->move_x += 10;
}